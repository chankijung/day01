package day01;

public class Ex02 {
	public static void main(String[] args) {
		System.out.println(100 + 200);
		System.out.println(10.13 + 1.221);
		// 큰따옴표 없이 숫자로만 써지면 숫자로 인식
		// ;는 세미콜론. 마침표로 이해
		
		System.out.println("백"+"\t"+100+"\n"+100);
		// 문자열과 숫자를 합치면 숫자도 문자열로 인식
		// 개행문자 = 이스케이프문자는 쌍따옴표 안에 있어야 함
		
		System.out.println("백"+"백");
		System.out.println("100"+"100");
		//문자에서 +는 문자를 이어써주는 기능
		
		
		
	}

}
